package main.java.com.lemsviat.javacore.chapter05;

public class Nested {
    public static void main(String[] args) {
        int i,j;
        for (i = 0; i <10; i++){
            for (j = i; j <10; j++) {
                System.out.print(i+" ");
            }
        System.out.println();}
    }
}
